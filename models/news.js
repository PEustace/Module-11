class News {
  constructor(id, headline, date, author, agency, description, imageUrl, category) {
    this.id = id;
    this.headline = headline;
    this.date = date;
    this.author = author;
    this.agency = agency;
    this.description = description;
    this.imageUrl = imageUrl;
    this.category = category;
  }
}

export default News;